Additional Information on the INed Editor

1) If INed is run under X windows, and the INed screen is split into
multiple sub-windows (using INed's window commands), then if resized
with X, all sub-windows other than the first will be lost.  Similarly, 
resizing will cause popup boxes to be lost.

Action:  Ensure that the X window containing the INed screen is
of an appropriate size before creating the INed sub-windows.

